#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		count;
	t_list	*n;

	count = 0;
	n = NULL;
	if (!lst)
		return (0);
	n = lst;
	while (n)
	{
		count++;
		n = n->next;
	}
	return (count);
}
