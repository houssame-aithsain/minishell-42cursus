#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t			i;
	unsigned char	*castedchar;

	castedchar = b;
	i = 0;
	while (i < len)
	{
		castedchar[i] = (unsigned char)c;
		i++;
	}
	return (b);
}
