#include "libft.h"

int	ft_tolower(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
	{
		if (c >= 65 && c <= 90)
			return (c + 32);
		else if (c >= 97 && c <= 122)
			return (c);
	}
	return (c);
}
